public class CalculatorLab {
//fields

//ctors

//methods
   public int add(int lhs, int rhs)
   {
      int sum = lhs + rhs;
      return sum;
   }
  
   public int subtract (int lhs, int rhs)
   {
      int difference = lhs - rhs;
      return difference;
   }
  
   public int multiply (int lhs, int rhs)
   {
      int product = lhs * rhs;
      return product;
   }
}